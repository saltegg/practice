class Solution {
    public void nextPermutation(int[] nums) {
        int l = 0;
        int r = nums.length - 1;
        for (int i = 0; i < nums.length - 1; i++) {
            if (nums[i] < nums[i + 1]) {
                l = i;
            }
        }
        for (int i = l + 1; i < nums.length; i++) {
            if (nums[i] > nums[l]) {
                r = i;
            }
        }
        swap(nums, r, l);
        Arrays.sort(nums, l + 1, nums.length);
    }

    private void swap(int[] nums, int i, int j) {
        int tmp = nums[i];
        nums[i] = nums[j];
        nums[j] = tmp;
    }
}
